def luasSegitiga(a, t=0):
	luas = a * t / 2
	return luas

alas = 10
tinggi = 20
print('Luas segitiga dg alas', alas, 'dan tinggi', tinggi, 'adalah', luasSegitiga(alas, tinggi))

